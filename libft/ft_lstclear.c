#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void*))
{
	t_list	*aux;

	while (*lst)
	{
		aux = *lst;
		del(aux->content);
		*lst = aux->next;
		free(aux);
	}
	*lst = NULL;
}
